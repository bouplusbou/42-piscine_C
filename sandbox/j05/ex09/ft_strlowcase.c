#include <stdio.h>

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ('A' <= str[i] && str[i] <= 'Z')
			str[i] += 32;
		i++;
	}
	return (str);
}

int		main()
{
	char	str[] = "LOw CaSE Me";

	printf("%s", ft_strlowcase(str));
	return (0);
}
