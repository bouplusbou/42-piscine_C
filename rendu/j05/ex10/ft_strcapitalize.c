char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (('a' <= str[i] && str[i] <= 'z')
			&& (str[i - 1] < 'A' || str[i - 1] > 'Z')
			&& (str[i - 1] < 'a' || str[i - 1] > 'z')
			&& (str[i - 1] < '0' || str[i - 1] > '9'))
		{
			str[i] -= 32;
		}
		if (('A' <= str[i] && str[i] <= 'Z')
			&& (('A' <= str[i - 1] && str[i - 1] <= 'Z')
			|| ('a' <= str[i - 1] && str[i - 1] <= 'z')
			|| ('0' <= str[i - 1] && str[i - 1] <= '9')))
		{
			str[i] += 32;
		}
		i++;
	}
	return (str);
}
