int		ft_str_is_printable(char *str)
{
	int	i;
	int	l;

	i = 0;
	l = 0;
	while (str[l])
		l++;
	if (l == 0)
		return (1);
	while (str[i])
	{
		if ((0 <= str[i] && str[i] <= 32) || (str[i] == 127))
			return (0);
		i++;
	}
	return (1);
}
