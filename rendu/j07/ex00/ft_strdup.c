#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*dest;
	int		l;
	int		i;

	l = 0;
	i = 0;
	while (src[l])
		l++;
	if (!(dest = (char*)malloc(sizeof(dest) * (l + 1))))
		return (0);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
